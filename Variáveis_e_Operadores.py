'''Peça aos alunos para criar um programa que solicite dois números do usuário, armazene esses valores em variáveis e, em seguida, exiba a soma, subtração, multiplicação e divisão desses números.'''

num = int(input('Digite um número:\n'))
num1 = int(input('Digite um número:\n'))

soma = num + num1
subtracao = num - num1
multiplicacao = num * num1
divisao = num / num1

print (soma, subtracao, multiplicacao, divisao)